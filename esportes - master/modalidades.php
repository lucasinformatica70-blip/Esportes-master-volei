    <h1>principais modalidades</h1>
    <p>Vôlei de Quadra: É a versão tradicional jogada em ginásios, com equipes de 6 jogadores de cada lado. A quadra é dividida por uma rede alta e
         o foco está na força, tática e especialização por posições (levantador, ponteiro, central, oposto e líbero).
</p>
    <p>Vôlei de Praia: Disputado em uma quadra de areia ao ar livre, geralmente em duplas (2 contra 2). 
        Exige muito mais preparo físico, velocidade e impulsão devido à instabilidade da areia, e os jogadores precisam dominar todos os fundamentos (passe, defesa e ataque).
</p>
    <p>Vôlei Sentado: Modalidade paralímpica destinada a pessoas com deficiência físico-motora.
         A quadra é menor e a rede é mais baixa. Os atletas jogam sentados e o deslocamento é feito com os próprios braços, mantendo sempre o quadril em contato com o solo no 
         momento de tocar a bola
</p>
    <p> Futevôlei: Criado nas praias brasileiras, mistura o vôlei de quadra com o futebol. É jogado em uma quadra de areia, mas é proibido o uso das mãos, braços e ombros.
         Os pontos são disputados usando os pés, cabeça, peito e coxas.
</p>
    <p>Câmbio (Vôlei Adaptado): Muito comum em centros esportivos para a terceira idade. A principal diferença é que a bola pode ser segurada/retida (o jogador não é obrigado a dar um toque rápido) antes de ser passada para o companheiro ou para a quadra adversária.
</p>
    <p>Biribol: É o "vôlei de piscina", jogado dentro da água. Exige muita movimentação e impulsão, tornando-se uma opção excelente e refrescante para o lazer.</p>
    <p>Wall Ball (Vôlei de Parede): Variação onde os jogadores rebatem a bola contra uma parede em vez de jogá-la por cima de uma rede.</p>
    <p>Rei/Rainha da Praia (King of the Court): Dinâmica em que um trio ou dupla enfrenta os outros. Quem pontua continua na quadra; quem perde a disputa sai para dar lugar a uma dupla que 
        aguarda fora.</p>
    <p>Vôlei de Pelotão: Permite um número maior de pessoas do que o limite oficial. Todos se posicionam em círculo ou em uma grande roda e evitam que a bola caia, 
        trabalhando o toque e a agilidadeem grupo.
</p>
<p>Passar de Primeira: Na descontração, os times abrem mão da regra de dar 3 toques (recepção, levantamento e cortada) e devolvem a bola para a quadra rival com apenas um toque, 
    exigindo muita visão de jogo e agilidade.</p>
    
